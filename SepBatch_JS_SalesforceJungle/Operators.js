//COMPOUND OPERATOR

let a = 5;
a = a + 10; 
console.log(a); // 15

a +=5;
console.log(a); //20

a++;  // 20 ---21
++a;  // 21 ---- 22
a--;  //22 ---21
--a;  //20---20
console.log(a++); //20 ---21
a += 10; 
console.log(++a);  // 31  ---32